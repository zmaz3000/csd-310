-- create the Supplier table 
CREATE TABLE supplier (
    supplier_id     	INT      	NOT NULL        AUTO_INCREMENT,
    supplier_name  		VARCHAR(75) NOT NULL, -- Changed company_name to supplier_name to keep same format as Distributor table
	street_address   	VARCHAR(75) NOT NULL,
    city			   	VARCHAR(75) NOT NULL,
    state			   	VARCHAR(75) NOT NULL,
	country				VARCHAR(75) NOT NULL,	
	zip_code			INT     	NOT NULL,	
	contact_first_name	VARCHAR(75) NOT NULL,	
	contact_last_name	VARCHAR(75) NOT NULL,	
	phone_number		VARCHAR(75) NOT NULL,	
	email_address		VARCHAR(75) NOT NULL,	
	
    PRIMARY KEY(supplier_id)
); 

-- create the Supplies table and set the foreign key
CREATE TABLE supplies (
    supply_id     		INT			NOT NULL        AUTO_INCREMENT,
    item_name			VARCHAR(75) NOT NULL,	
	item_description	VARCHAR(75) NOT NULL,	
	units_on_hand		INT			NOT NULL,	
	supplier_id			INT     	NOT NULL,	
	
    PRIMARY KEY (supply_id),
	
	CONSTRAINT fk_supplier_supplies -- Can't have duplicate constraints accross an entire database
    FOREIGN KEY(supplier_id)
        REFERENCES supplier(supplier_id)
); 

-- create the Supply Order table and set the foreign key
CREATE TABLE supply_order (
    supply_order_id     	INT     NOT NULL        AUTO_INCREMENT,
    total_cost				FLOAT  	NOT NULL, -- Changed INT to FLOAT
	order_date				DATE  	NOT NULL, -- Changed INT to DATE
	tracking_number			INT     NOT NULL,
	estimated_delivery_date	DATE	NULL, -- Changed INT to DATE 
	actual_delivery_date	DATE	NULL, -- Changed INT to DATE
	supplier_id     		INT  	NOT NULL,
		
	PRIMARY KEY(supply_order_id),
	
	CONSTRAINT fk_supplier_order -- Can't have duplicate constraints accross an entire database
    FOREIGN KEY(supplier_id)
        REFERENCES supplier(supplier_id)
);

-- create the Supply Order Details table and set the foreign keys
CREATE TABLE supply_order_details(
    supply_order_details_id	INT     NOT NULL        AUTO_INCREMENT,
    units_ordered			INT    	NOT NULL,	
	unit_price				FLOAT  	NOT NULL,	-- Changed INT to FLOAT, and name from unit_cost to unit_price to show it is a duplicate entry with Supplies table
	supply_order_id			INT		NOT NULL,
    supply_id				INT     NOT NULL,   -- Changed supply_id to appear last in the table. Previously supply_order_id appeared last	
     
    PRIMARY KEY (supply_order_details_id),
	
	CONSTRAINT fk_supplies
    FOREIGN KEY (supply_id)
        REFERENCES supplies(supply_id),
		
	CONSTRAINT fk_supply_order
    FOREIGN KEY (supply_order_id)
        REFERENCES supply_order(supply_order_id)
); 

-- create the Wine table 
CREATE TABLE wine (
    wine_id     		INT         NOT NULL        AUTO_INCREMENT,
    wine_name   		VARCHAR(75) NOT NULL,
    wine_description	VARCHAR(75)	NOT NULL,
	units_on_hand   	INT     	NOT NULL,
	cost_per_unit		FLOAT    	NOT NULL, -- I'm assuming this is the "base cost" of Bacchus producing the bottle of wine, maybe change to production_cost
	
    PRIMARY KEY(wine_id)
); 
 
-- create the Distributor table 
CREATE TABLE distributor (
    distributor_id     	INT         NOT NULL        AUTO_INCREMENT,
    distributor_name	VARCHAR(75) NOT NULL,
	street_address   	VARCHAR(75) NOT NULL,
	city				VARCHAR(75) NOT NULL,
    state				VARCHAR(75) NOT NULL,
    country				VARCHAR(75) NOT NULL,	
	zip_code			INT     	NOT NULL,	
	contact_first_name	VARCHAR(75) NOT NULL,	
	contact_last_name	VARCHAR(75) NOT NULL,	
	phone_number		VARCHAR(75) NOT NULL,	
	email_address		VARCHAR(75) NOT NULL,
	wine_id				INT			NOT NULL,
	
    PRIMARY KEY(distributor_id),
    
    CONSTRAINT fk_wine_distributor -- Can't have duplicate constraints accross an entire database
    FOREIGN KEY(wine_id)
		REFERENCES wine(wine_id)
); 

-- create the Wine Order table and set the foreign key
CREATE TABLE wine_order(
    wine_order_id     		INT     NOT NULL        AUTO_INCREMENT,
    total_cost				FLOAT  	NOT NULL, -- Changed INT to FLOAT
	order_date				DATE  	NOT NULL, -- Changed INT to DATE
	tracking_number			INT     NOT NULL, -- added to mirror feature of Supply Order table
	estimated_delivery_date	DATE	NULL, -- Changed INT to DATE 
	actual_delivery_date	DATE	NULL, -- Changed INT to DATE
	distributor_id     		INT	    NOT NULL, 
		
	PRIMARY KEY(wine_order_id),
	
	CONSTRAINT fk_distributor_id
    FOREIGN KEY(distributor_id)
        REFERENCES Distributor(distributor_id)
);

-- create the Wine Order Details table and set the foreign keys
CREATE TABLE wine_order_details(
    wine_order_details_id	INT     NOT NULL        AUTO_INCREMENT,
    units_ordered			INT    	NOT NULL,	
	sales_price				FLOAT  	NOT NULL,	-- Changed INT to FLOAT, and name from unit_cost to sale_price to be able to calculate profits
	wine_order_id			INT		NOT NULL,
    wine_id					INT     NOT NULL,	
     
    PRIMARY KEY(wine_order_details_id),
	
	CONSTRAINT fk_wine_order_details
    FOREIGN KEY(wine_id)
        REFERENCES wine(wine_id),
		
	CONSTRAINT fk_wine_order
    FOREIGN KEY(wine_order_id)
        REFERENCES wine_order(wine_order_id)
); 
 
-- create the Employees table (made quite a few changes)
CREATE TABLE employees (
    employee_id    	INT         NOT NULL        AUTO_INCREMENT,
    first_name		VARCHAR(75) NOT NULL,
	last_name		VARCHAR(75) NOT NULL, 
	date_of_birth	DATE		NOT NULL, -- Added date of birth for Labor Law/HR purposes
	phone_number	VARCHAR(75) NOT NULL,	
	email_address	VARCHAR(75) NOT NULL,	
	street_address  VARCHAR(75) NOT NULL,
	city			VARCHAR(75) NOT NULL,
    state			VARCHAR(75) NOT NULL,
    country			VARCHAR(75) NOT NULL,	
	zip_code		INT     	NOT NULL,	
	hire_date		DATE		NOT NULL, -- Added for seniority, work anniversaries, raise opportunities, 
	job_title		VARCHAR(75) NOT NULL, -- Added because there aren't really departments, but there are job responsibilities
	hourly_wage		FLOAT		NOT NULL, -- Added to calculate how much they get paid from their hours worked, not witholding taxes.
	employee_status	VARCHAR(75)	NOT NULL, -- Added to be able to keep records of past employees (active, retired, terminated)
	
    PRIMARY KEY(employee_id)
); 

-- create the Timecards table and set the foreign key (changing identifiers, removed breaks, and changed data types)
CREATE TABLE timecards (
    timecard_id   				INT     	NOT NULL        AUTO_INCREMENT,
    date_posted					DATE		NOT NULL, -- changed/new
	hours_worked_monthly		INT			NOT NULL, -- changed/new
	pto_hours					INT			NOT NULL, -- changed/new
	unpaid_time_taken			INT			NOT NULL, -- changed/new
    total_hours_worked			INT			NOT NULL,
	employee_id    				INT         NOT NULL,
	
	PRIMARY KEY(timecard_id),
	
	CONSTRAINT fk_employee_id
    FOREIGN KEY(employee_id)
        REFERENCES employees(employee_id)
);


